                            <span>Quelle est la cause du réchauffement climatique actuel ?</span>
                            <h2>Causes du réchauffement climatique</h2>
                            <p>
                               <Strong>L’effet de serre </Strong>  est la principale cause  du réchauffement climatique. À l’origine, c’est un phénomène naturel qui contribue au maintien du niveau moyen de la température. Cependant, à cause de la concentration de plus en plus importante des gaz à effet de serre dans l’atmosphère liés aux activités humaines, il s’intensifie provoquant un réchauffement climatique:
                            
                            </p><br>

                            <ul style=" margin-left: 25px;">
                                <li>L’accumulation du dioxyde de carbone (CO<sub>2</sub>) dans l’atmosphère contribue pour 2/3 de l’augmentation de l’effet de serre induite par les activités humaines (combustion de gaz, de pétrole, déforestation, cimenteries, etc.). C’est pourquoi on mesure usuellement l’effet des autres gaz à effet de serre en équivalent CO<sub>2</sub> (eq. CO<sub>2</sub>). Les émissions de CO<sub>2</sub> actuelles auront un impact sur les concentrations dans l’atmosphère et sur la température du globe pendant des dizaines d’années, car sa durée de vie dans l’atmosphère est supérieure à la centaine d’années.</li>
                                <li>Le méthane (CH<sub>4</sub>)&nbsp;: les élevages des ruminants, les rizières inondées, les décharges d’ordures et les exploitations pétrolières et gazières constituent les principales sources de méthane induites par les activités humaines. La durée de vie du méthane dans l’atmosphère est de l’ordre de 12 ans.</li>
                                <li>Le protoxyde d’azote (N<sub>2</sub>O) provient des engrais azotés et de certains procédés chimiques. Sa durée de vie est de l’ordre de 120 ans.</li>
                                <li>L’hexafluorure de soufre (SF<sub>6</sub>) a une durée de vie de 50&nbsp;000 ans dans l’atmosphère.</li>
                            </ul>

