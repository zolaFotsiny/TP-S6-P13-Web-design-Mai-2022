                        <span>Qu'est-ce que le réchauffement climatique ?</span>
                        <h2>Définition simple du réchauffement climatique</h2>
                        <p>Le réchauffement climatique est un phénomène global de transformation du climat caractérisé par une augmentation générale des températures moyennes (notamment liée aux activités humaines), et qui modifie durablement les équilibres météorologiques et les écosystèmes.</p>                        
                        <p>Lorsque l’on en parle aujourd’hui, il s’agit du phénomène d’augmentation des températures qui se produit sur Terre depuis 100 à 150 ans. Depuis le début de la Révolution Industrielle, les températures moyennes sur terre ont en effet augmenté plus ou moins régulièrement. En 2016, la température moyenne sur la planète terre était environ 1 à 1.5 degrés au dessus des températures moyennes de l’ère pré-industrielle (avant 1850).</p>

                        <h2>Définition scientifique  du réchauffement climatique</h2>
                        <p>De façon plus précise, lorsque l’on parle du réchauffement climatique, on parle de l’augmentation des températures liées à l’activité industrielle et notamment à l’effet de serre : on parle donc parfois du réchauffement climatique dit « d’origine anthropique » (d’origine humaine). Il s’agit donc d’une forme dont les causes ne sont pas naturelles mais économiques et industrielles.</p>                        
                        <p>De nombreux scientifiques étudient ce phénomène et tentent de comprendre comment les activités des sociétés humaines provoque ce réchauffement. Ces scientifiques sont regroupés au sein du GIEC (Groupe International d’Experts sur le Climat), et ils publient régulièrement des rapports étudiant l’évolution du réchauffement climatique (voir plus bas).</p>
                        
